        private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
            // TODO add your handling code here:
            String nombreCanciones = JOptionPane.showInputDialog("Escribe una cancion a buscar");
            if(nombreCanciones.equals("") || nombreCanciones == null){
                return;
            }

            if(listaMusica.contains(nombreCanciones)){
                JOptionPane.showMessageDialog(this, "Si se encuentra la cancion");
            } else{
                JOptionPane.showMessageDialog(this, "No se encuentra la cancion");
            }
        }//GEN-LAST:event_btnBuscarActionPerformed

