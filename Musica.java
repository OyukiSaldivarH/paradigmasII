        private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
            // TODO add your handling code here:
            String nombreCanciones = JOptionPane.showInputDialog("Escribe un nombre de una cancion:");
            if(nombreCanciones.equals("") || nombreCanciones == null){
                return;
            }
            listaMusica.add(nombreCanciones);
            llenarListaMusica();
        }//GEN-LAST:event_btnAgregarActionPerformed

        private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
            // TODO add your handling code here:
            String nombreCanciones = JOptionPane.showInputDialog("Escribe una cancion a borrar: ");
            if(nombreCanciones.equals("") || nombreCanciones == null){
                return;
            }
            Iterator<String> iterador = listaMusica.iterator();
            while(iterador.hasNext()){
                if(nombreCanciones.equals(iterador.next())){
                    iterador.remove();
                    JOptionPane.showMessageDialog(this, "La Cancion se ha eliminado");
                }
            }
            llenarListaMusica();
        }//GEN-LAST:event_btnEliminarActionPerformed

